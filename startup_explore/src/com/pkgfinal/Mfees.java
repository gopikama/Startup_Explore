    public void getValue(){
        Float xconsult = null;
        Float xtotal = null;
        
        System.out.println("test");
        
        try{
            
        Class.forName("com.mysql.jdbc.Driver");
         
        Connection conn = DriverManager.getConnection("jdbc:mysql://root@localhost:3306/project?autoReconnect=true&useSSL=false","arundhati","arundhati");
        
        String sql1="select sum(fees) as total from mfees where memail='"+this.memail+"';";
        String sql2="select fees as fees from mentor where email='"+this.memail+"';";
        
        PreparedStatement pst1=conn.prepareStatement(sql1);
        PreparedStatement pst2=conn.prepareStatement(sql2);
        
        ResultSet rs1=pst1.executeQuery();
        if(rs1.next())
            xtotal=rs1.getFloat("total");
        
        ResultSet rs2=pst2.executeQuery();
        if(rs2.next())
            xconsult=rs2.getFloat("fees");
         
        consulttf.setText(String.valueOf(xconsult));
        totaltf.setText(String.valueOf(xtotal));
        
        pst1.close();
        pst2.close();
        conn.close();
        
      
            
    }
        catch(ClassNotFoundException | SQLException e)
     {
         JOptionPane.showMessageDialog(null, e.getMessage() ,"Error", 1);
          System.out.println(e);
         

     }

        
        
      
    }     
            
    
    
    
    
