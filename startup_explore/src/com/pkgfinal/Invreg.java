       try
     {   //System.out.println("start");
         Class.forName("com.mysql.jdbc.Driver");
         //System.out.println("start1");
         Connection conn = DriverManager.getConnection("jdbc:mysql://root@localhost:3306/project?autoReconnect=true&useSSL=false","arundhati","arundhati");
         System.out.println("Connection estd");
         PreparedStatement stmt=conn.prepareStatement("insert into investor values(?,?,?,?,?,?,?)");

         String xiname=iname.getText();
         String xemail=email.getText();
         String xpw=pw.getText();
         int xcontact=Integer.parseInt(contact.getText());
         String xloc=location.getText();
         String xmar=market.getText();
         Float xcap=Float.parseFloat(capital.getText());
                
       stmt.setString(1,xiname);
       stmt.setString(2,xemail);
       stmt.setString(3,xpw);
       stmt.setInt(4,xcontact);
       stmt.setString(5,xloc);
       stmt.setString(6,xmar);
       stmt.setFloat(7,xcap);
       
       
       int i=stmt.executeUpdate();
       System.out.println(i);

        stmt.close();
        conn.close();

     }
